def create_table(dynamodb):
    try:
        table = dynamodb.create_table(
            TableName=TABLE_NAME,
            KeySchema=[
                {'AttributeName': 'Year',        'KeyType': 'HASH'},
                {'AttributeName': 'OverallPick', 'KeyType': 'RANGE'},
            ],
            AttributeDefinitions=[
                {'AttributeName': 'Year',        'AttributeType': 'S'},
                {'AttributeName': 'OverallPick', 'AttributeType': 'N'},
            ],
            BillingMode='PAY_PER_REQUEST',
        )
        print(f'Creating table {TABLE_NAME}...')
        table.wait_until_exists()
        print('Table created.')
        return table
    except ClientError as e:
        if e.response['Error']['Code'] == 'ResourceInUseException':
            print(f'Table {TABLE_NAME} already exists, using existing.')
            return dynamodb.Table(TABLE_NAME)
        raise


def seed(table):
    items = []
    for row in PICKS:
        overall, round_, pick, team, player, position, status = row
        items.append({
            'Year':        '2026',
            'OverallPick': overall,
            'Round':       round_,
            'Pick':        pick,
            'Team':        team,
            'Player':      player,
            'Position':    position,
            'Status':      status,
        })

    # Batch write in groups of 25
    with table.batch_writer() as writer:
        for item in items:
            writer.put_item(Item=item)

    print(f'Seeded {len(items)} picks into {TABLE_NAME}.')


